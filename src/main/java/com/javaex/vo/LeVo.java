package com.javaex.vo;

public class LeVo {

	private int no;
	private String comment;
	
	public LeVo() {
		super();
	}

	public LeVo(int no, String comment) {
		super();
		this.no = no;
		this.comment = comment;
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}
	
	
	
	
	
}
